package com.zr.gansu.manage.configs;

/**
 * @description: 上传Settings
 * @author: KaiZhang
 * @create: 2019-01-04 11:42
 **/
public class UploadSettings {
    /**
     * 上传基础路径
     */
    private String baseStoragePath;

    /**
     * 资源视频上传路径
     */
    private String resourcesVideoStoragePath;

    /**
     * 课程资源视频上传路径
     */
    private String courseVideoStoragePath;

    /**
     * 图片上传路径
     */
    private String imgStoragePath;

    /**
     * 文件下载仓库地址
     */
    private String repoPath;

    public String getBaseStoragePath() {
        return baseStoragePath;
    }

    public void setBaseStoragePath(String baseStoragePath) {
        this.baseStoragePath = baseStoragePath;
    }

    public String getImgStoragePath() {
        return imgStoragePath;
    }

    public void setImgStoragePath(String imgStoragePath) {
        this.imgStoragePath = imgStoragePath;
    }

    public String getRepoPath() {
        return repoPath;
    }

    public void setRepoPath(String repoPath) {
        this.repoPath = repoPath;
    }

    public String getResourcesVideoStoragePath() {
        return resourcesVideoStoragePath;
    }

    public void setResourcesVideoStoragePath(String resourcesVideoStoragePath) {
        this.resourcesVideoStoragePath = resourcesVideoStoragePath;
    }

    public String getCourseVideoStoragePath() {
        return courseVideoStoragePath;
    }

    public void setCourseVideoStoragePath(String courseVideoStoragePath) {
        this.courseVideoStoragePath = courseVideoStoragePath;
    }
}
